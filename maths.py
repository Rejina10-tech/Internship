import math

numbers = [5, 8, 2, 10, 3]

max_value = max(numbers)
min_value = min(numbers)

print("Maximum value:", max_value) #prints 10
print("Minimum value:", min_value) #prints 2



absolute_value = abs(-10)
print("Absolute value:", absolute_value) #prints 10

square_root = math.sqrt(25)
print("Square root:", square_root) #prints 5

x = math.pi

print(x) #prints 3.14